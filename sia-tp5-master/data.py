# ---- ConversiÃ³n a binario (7x5 -> vector de 35)
def pattern_to_array(rows):
    arr = np.zeros((7,5), dtype=np.float32)
    for r, val in enumerate(rows):
        for c in range(5):
            if (val >> c) & 1:
                arr[r, 4-c] = 1.0
    return arr

